extern FNN F39_out;
static void C39(REAL * C)
{
REAL* V=va_out;
REAL S[10];                                                                 
     
S[0]=V[10]*V[10];
S[1]=V[14]*V[14];
S[2]=V[12]*V[12];
S[3]=V[21]*V[21];
C[5]=+S[0]*(V[20]*(V[20]*(S[0]-S[1]-S[2])+V[21]*(2*(S[1]-S[2]-S[0])))+S[3]*(
 S[0]-S[1]-S[2]))+S[2]*(S[1]*(V[20]*(2*V[21]+V[20])+S[3]));
C[4]=+V[20]*(S[0]*(4*V[21]-2*V[20])+S[1]*(2*V[20]-4*V[21]))+S[3]*(2*(S[1]-
 S[0]));
C[3]=+V[20]*(S[0]*(4*V[21]-2*V[20])+S[2]*(4*V[21]+2*V[20]))+S[3]*(2*(S[2]-
 S[0]));
C[2]=+V[20]*(4*V[20]-8*V[21])+4*S[3];
S[4]=V[15]*V[15];
S[5]=V[2]*V[2]*V[2]*V[2];
C[1]=+S[4]*S[5];
S[6]=V[28]*V[28];
S[7]=V[26]*V[26];
S[8]=V[17]*V[17];
S[9]=V[1]*V[1]*V[1]*V[1];
C[0]=+4*S[6]*S[7]*S[8]*S[9];
}
REAL F39_out(double GG,REAL*DP,REAL*Q0,REAL*Q1,REAL*Q2)
{
/*
          G                 U     !  U                 G             
        -1---\           /==<=====!==<==\           /---1-           
          P1 |           |  P3    !  P3 |           |  P1            
             |           |        !     |           |                
          C  |  t     W+ |  d     !  d  |  W+    t  |  C             
        ==<==@==<==@-2<--@==>=====!==>==@--<3-@==<==@==<==           
          P2    P6 |  P7    P4    !  P4   -P9 | -P8    P2            
                   |              !           |                      
                   |        B     !  B        |                      
                   \========<=====!==<========/                      
                            P5    !  P5                              
*/
REAL N,D,R;
REAL * V=va_out;
static REAL C[6];REAL S[1];                                                 
     
if(CalcConst) C39(C);
S[0]=GG*GG;
N=-C[0]*S[0];
D=+C[1];
R=+DP[0]*(DP[0]*(DP[4]*(C[2]*(DP[3]-DP[0]+DP[4])+C[4])-C[3]*DP[3])+DP[3]*(
 C[3]*(DP[3]+DP[4])+C[5]));
R*=(N/D)*Q2[1]*Q2[2];
 if(cb_coeff_out)
 {
  cb_coeff_out[2] += (R*9)/(8);
 }
 return R;
}
