static void C61(REAL * C)
{
REAL* V=va_out;
REAL S[9];                                                                  
     
S[0]=V[18]*V[18];
S[1]=V[17]*V[17];
S[2]=V[7]*V[7]*V[7]*V[7];
C[5]=+S[2]*(S[0]+S[1]);
S[3]=V[7]*V[7];
C[4]=+S[3]*(4*(S[0]+S[1]));
C[3]=+4*(S[0]+S[1]);
C[2]=+S[3]*(2*(S[0]+S[1]));
S[4]=V[12]*V[12];
S[5]=V[2]*V[2]*V[2]*V[2];
C[1]=+S[4]*S[5];
S[6]=V[22]*V[22];
S[7]=V[14]*V[14];
S[8]=V[1]*V[1]*V[1]*V[1];
C[0]=+48*S[6]*S[7]*V[9]*V[8]*S[8];
}
REAL F61_out(double GG,REAL*DP,REAL*Q0,REAL*Q1,REAL*Q2)
{
/*
                           n2     !  n2                              
                        /==>======!==>==\                            
                        |  P4     !  P4 |                            
                        |         !     |                            
         c     t     W+ |  E2     !  E2 |  W+    t     c             
       ==>==@==>==@-4>--@==<======!==<==@-->6-@==>==@==>==           
         P1 |  P8 |  P9    P5     !  P5   -PC | -PB |  P1            
            |     |               !           |     |                
            |     |        b      !  b        |     |                
         ~03|P7   \========>======!==>========/  ~03|-PA             
            1              P6     !  P6             5                
            |                     !                 |                
         G  |              G      !  G              |  G             
       -2---@-------------3-------!---3-------------@---2-           
         P2                P3     !  P3                P2            
*/
REAL N,D,R;
REAL * V=va_out;
static REAL C[6];REAL S[4];                                                 
     
if(CalcConst) C61(C);
S[0]=GG*GG*GG*GG;
N=+C[0]*S[0];
S[1]=V[23]*V[23]*V[23]*V[23];
D=+C[1]*S[1];
S[2]=V[23]*V[23];
S[3]=DP[2]*DP[2];
R=+S[3]*(C[2]*(DP[9]*(DP[13]*(DP[5]-DP[3]-DP[4]-DP[6]-DP[7]+DP[8])+DP[14]*(
 DP[5]-DP[3]-DP[4]-DP[6]-DP[7]+DP[8])+S[2]*(DP[12]-DP[10]-DP[11])+DP[9]*(
 DP[10]+DP[11]-DP[12]))+S[2]*(DP[13]*(DP[6]+DP[7]-DP[8])+DP[14]*(DP[3]+
 DP[4]-DP[5])))+S[2]*(DP[13]*(S[2]*(C[3]*(DP[8]-DP[6]-DP[7]))+C[4]*(DP[3]+
 DP[4]-DP[5])))+DP[9]*(C[5]*(DP[10]+DP[11]-DP[12])));
R*=(N/D)*Q2[7]*Q2[1]*Q2[2];
if(gswidth_out ) R=R*Q0[3];
 if(cb_coeff_out)
 {
  cb_coeff_out[0] += (R*-9)/(-16);
  cb_coeff_out[1] += (R*-9)/(-16);
 }
 return R;
}
